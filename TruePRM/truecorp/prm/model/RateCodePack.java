package truecorp.prm.model;

/**
 *
 * @author Jennarong Pinjai
 */
public class RateCodePack {
    
      String rate;
      String cd  ;
      String rateCd;
      String text;
      String rateCdSeq;
      String descriptionSeq;

    public String getRate() {
        return rate;
    }

    public void setRate(String rate) {
        this.rate = rate;
    }

    public String getCd() {
        return cd;
    }

    public void setCd(String cd) {
        this.cd = cd;
    }

    public String getRateCd() {
        return rateCd;
    }

    public void setRateCd(String rateCd) {
        this.rateCd = rateCd;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    public String getRateCdSeq() {
        return rateCdSeq;
    }

    public void setRateCdSeq(String rateCdSeq) {
        this.rateCdSeq = rateCdSeq;
    }

   
    public String getDescriptionSeq() {
        return descriptionSeq;
    }

    public void setDescriptionSeq(String descriptionSeq) {
        this.descriptionSeq = descriptionSeq;
    }
      
      
      
    
    
    
}
